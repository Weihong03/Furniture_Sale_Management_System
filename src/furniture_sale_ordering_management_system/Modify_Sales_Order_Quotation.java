      private boolean modifySalesQuotation(String orderID, String Amount, Date date, String Salesperson, String Confirmation, String Officer, String Invoice, String Status) {
    try {
        Path inputFile = Path.of("Data/Sales_Quotation.txt");

        List<String> lines = Files.readAllLines(inputFile, StandardCharsets.UTF_8);

        boolean found = false;
        SimpleDateFormat dateFormat = new SimpleDateFormat("dd MMM yyyy");
        String formattedDate = dateFormat.format(date); // Format the date

        for (int i = 0; i < lines.size() - 8; i += 9) {
            String line = lines.get(i);
            if (line.equals("ID: " + orderID)) {
                // Modify the existing booking
                lines.set(i, "ID: " + orderID);
                lines.set(i + 1, "Amount: " + Amount);
                lines.set(i + 2, "Date: " + formattedDate + ","); // Use the formatted date
                lines.set(i + 3, "Salesperson: " + Salesperson);
                lines.set(i + 4, "Confirmation: " + Confirmation);
                lines.set(i + 5, "Officer: " + Officer);
                lines.set(i + 6, "Invoice: " + Invoice);
                lines.set(i + 7, "Status: " + Status);
                found = true;
                break;
            }
        }

        if (!found) {
            System.out.println("Profile not found.");
            return false;
        }

        Files.write(inputFile, lines, StandardCharsets.UTF_8, StandardOpenOption.WRITE, StandardOpenOption.TRUNCATE_EXISTING);

        return true;
    } catch (IOException e) {
        e.printStackTrace();
        return false;
    }
}
    private void jButton_modifyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_modifyActionPerformed
          String orderID = jTextField_id.getText();
    String Amount = jTextField_amount.getText();
    Date date = jDateChooser.getDate();
    String Salesperson = jTextField_salesperson.getText();
    String Confirmation = jTextField_confirmation.getText();
    String Officer = jTextField_officer.getText();
    String Invoice = jTextField_invoice.getText();
    String Status = jTextField_status.getText();

    // Validate input fields
    if (orderID.isEmpty() || Amount.isEmpty() || Status.isEmpty() || Salesperson.isEmpty() || Confirmation.isEmpty() || Officer.isEmpty() || Invoice.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Please fill in all the required fields.", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    // Perform the booking
    boolean isModified = modifySalesQuotation(orderID, Amount, date, Salesperson, Confirmation, Officer, Invoice, Status);

    if (isModified) {
        JOptionPane.showMessageDialog(this, "Sales Order Quotation modified successfully!", "Success", JOptionPane.INFORMATION_MESSAGE);
    } else {
        JOptionPane.showMessageDialog(this, "Failed to modify the Sales Order Quotation. Please try again.", "Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_jButton_modifyActionPerformed

